package JAVA_KK;

import java.util.Scanner;

public class loops2 {
  public static void main(String[] args) {
    Scanner in=new Scanner(System.in);
    System.out.println("Enter the Range :");
    int n=in.nextInt();

    for(int i=0;i<=n;i++)
    {
      System.out.print(i+" ");
    }

  }
  
}
